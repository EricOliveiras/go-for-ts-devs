let num: number = 10;
if (num > 0) {
  console.log("Number is positive");
} else if (num < 0) {
  console.log("Number is negative");
} else {
  console.log("Number is zero");
}

// Switch statement
let grade: string = "A";
switch (grade) {
  case "A":
    console.log("Excellent!");
    break;
  case "B":
    console.log("Good!");
    break;
  case "C":
    console.log("Average");
    break;
  default:
    console.log("Below Average");
}
